n1 = int(input("Enter the 1st number: "))
n2 = int(input("Enter the 2nd number: "))
if n2 > n1:
    print(f"The second number ({n2}) is higher than the first ({n1}) ")
else:
    print(f"The first number ({n1}) is higher than the second ({n2}) ")
